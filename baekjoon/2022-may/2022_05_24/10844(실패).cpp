#include <bits/stdc++.h>

using namespace std;

long long dp[101];

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int N;
    cin >> N;
    dp[1] = 9;
    dp[2] = 17; // (dp[1]-1)*2 + 1
    //dp[3] = 32; // (dp[2]-2)*2 + 2
    for (int i = 3; i <= N; i++) {
        dp[i] = ((dp[i - 1] - (i - 1)) * 2 + i - 1) % 1000000000;
    }
    cout << dp[N];

    return 0;
}
